    <h3>C3 Perduli</h3><p> <blockquote class="blq-belakang">Aksi moral untuk mendukung kelompok masyarakat, khususnya remaja dan anak yang memiliki keterbatasan dalam mengembangkan kreatifitas, misalnya anak jalanan, anak di Lapas Anak dan anak-anak di panti asuhan.</blockquote></p>

